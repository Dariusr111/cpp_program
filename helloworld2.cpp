// my second program in C++
#include <iostream>
using namespace std;

int main ()
{
  cout << "Hello World!\n"; // \n ("\"-escape "n"-new line character)
  cout << "I'm a C++ program\n";
  cout << "I'm a C++\t program\n"; // \t Creates a horizontal tab
  cout << "I'm a C++\\ program\n"; // \\ Inserts a backslash character (\)
  cout << "They call him \"Johnny\"."; // \" Inserts a double quote character
  return 0;
}